import numpy as np

# dot production of two vector
a = np.array([2,1])
b = np.array([1,2])
print(np.dot(a, b))

a = np.array([2,1])
b = np.array([1,-4])
print(np.dot(a, b))

a = np.array([2,1])
b = np.array([-3,-4])
print(np.dot(a, b))

a = np.array([1,2,3,4,5])
b = np.array([5,4,3,2,1])
print(np.dot(a, b))